namespace MathOperationsApp
{
    public class MathOperations
    {
        // Method to add 5 to the input number
        public int AddFive(int number)
        {
            return number + 5;
        }

        // Method to multiply the input number by 2
        public int MultiplyByTwo(int number)
        {
            return number * 2;
        }

        // Method to subtract 10 from the input number
        public int SubtractTen(int number)
        {
            return number - 10;
        }
    }
}